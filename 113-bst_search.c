#include "binary_trees.h"

/**
 * bst_search - searching value in the tree
 * @tree: root pointer
 * @value: searched val
 * Return:  node of int equal or null
 */
bst_t *bst_search(const bst_t *tree, int value)
{
	bst_t *node = (bst_t *)tree;

	if (!tree)
		return (NULL);
	while (node)
	{
		if (value == node->n)
			return (node);
		if (value < node->n)
			node = node->left;
		else if (value > node->n)
			node = node->right;
	}

	return (NULL);
}
